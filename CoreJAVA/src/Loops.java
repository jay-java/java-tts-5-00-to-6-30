
public class Loops {
	public static void main(String[] args) {
		System.out.println("for loop");
		for(int i=6;i<=5;i++) {
			System.out.println(i);
		}
		System.out.println("while loop");
		int j=6;
		while(j<=5) {
			System.out.println(j);
			j++;
		}
		System.out.println("do while");
		int k=6;
		do {
			System.out.println(k);
			k++;
		}
		while(k<=5);
	}
}
